function fromString(s: string): Board {
  const b: Board = new Array(81).fill(0);
  for (let i = 0; i < 81; i++) {
    const ch = s[i];
    b[i] = ch >= "1" && ch <= "9" ? Number(ch) : 0;
  }
  return b;
}

export const SAMPLE_PUZZLES: Board[] = PUZZLES.map(fromString);
